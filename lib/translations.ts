export function getTranslation(language: string, key: string): string {
  const langCode =
    language === "English"
      ? "en"
      : language === "Spanish"
        ? "es"
        : language === "Mandarin"
          ? "zh"
          : language === "Hindi"
            ? "hi"
            : "ar"
  return (TRANSLATIONS[langCode as keyof typeof TRANSLATIONS] as Record<string, string>)[key] || key
}
